// 以下是一个简单的C语言程序，用于生成九九乘法表：
#include <stdio.h>

int main() {
    int i, j;

    // 生成九九乘法表
    for (i = 1; i <= 9; i++) {
        for (j = 1; j <= i; j++) {
            printf("%d*%d=%-2d  ", j, i, i*j); // 使用%-2d保持对齐
        }
        printf("\n");
    }    

    return 0;
}

